
public class whileAndDowhileLoop {
    public static void main(String[] args) {
        int num = 1;
        while (num <= 10) { // while loop
            System.out.println(num);
            num++;
        }

        int i = 1;
        do { // Do While loop
            System.out.println(i);
            i++;
        } while (i <= 10);
    }
}
